import { DomainEvent } from "mailgun.js";
import { MG_EVENT_TABLE } from "../../_models/emailing/constants/MG_EVENT_TABLE.const";

export const getEventType = (item: DomainEvent) => {
  return item.event === "failed"
    ? MG_EVENT_TABLE[`failed-${item.severity}`]
    : MG_EVENT_TABLE[item.event];
};
